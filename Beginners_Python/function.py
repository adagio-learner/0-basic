# Parameters

# Default Parameter
def say_hello(first_name='mukesh', last_name='mavai'):
    print(f'helllooo {first_name} {last_name}')

# Positional Arguments
say_hello('akash', 'mavai')
say_hello('happy', 'birthday')

# Keyword Arguments
say_hello(last_name='you', first_name='To')
say_hello()
say_hello('birthday')